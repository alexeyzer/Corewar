#include "libft.h"

int				ft_word_count(char const *s, char c)
{
	int		ct;

	ct = 0;
	while (*s)
	{
		if (*s == c)
			s++;
		else
		{
			ct++;
			while (*s && *s != c)
				s++;
		}
	}
	return (ct);
}
